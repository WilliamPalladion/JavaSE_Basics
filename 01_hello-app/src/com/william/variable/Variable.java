package com.william.variable;

public class Variable {
    public static void main(String[] args) {
        // Variable: DataType Name = value;
        double money = 6.0;
        System.out.println(money);

        // e.g. receive money
        money = money + 4.0;
        System.out.println(money);

        System.out.println("-------------------------");
        // change age
        int age = 21;
        System.out.println(age);

        age = 25;
        System.out.println(age);
    }
}
